
public class Virag {
    private int seed;
    private int max;

    public Virag(int seed, int max) {
        this.seed = seed;
        this.max = max;
    }

    public void setMax(int max) {
        this.max = max;
    }

    public int getSeed() {
        return seed;
    }

    public void setSeed(int seed) {
        this.seed = seed;
    }

    public int getMax() {
        return max;
    }

}
